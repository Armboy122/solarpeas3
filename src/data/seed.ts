const usageSeries = (
  months: string[],
  kwhValues: number[],
  billFactor = 4.15,
): MonthlyUsage[] =>
  months.map((month, index) => ({
    month,
    kwh: kwhValues[index],
    bill: Math.round(kwhValues[index] * billFactor),
  }))

